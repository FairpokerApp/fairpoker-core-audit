// Single source of truth for the files that make up the public Fair Poker
// release/source package.
//
// THREE consumers depend on this module and MUST agree, or an honest auditor who
// extracts the archive and recomputes the fingerprint will get a different value
// (the "drift" bug). To make drift impossible they share the same data + rules:
//   - scripts/generate-release-metadata.js  computes the published sourceFingerprint
//   - scripts/create-source-release.js       builds the downloadable .tar.gz
//   - the reproducible build extracts that .tar.gz and rebuilds the Game client
//
// Rule that keeps fingerprint and archive consistent:
//   archive(files)     = includedRoots walked, minus isExcludedFromRelease()
//   fingerprint(files) = the same set, additionally skipping binary assets
//                        (images) whose bytes need not be fingerprinted
// Because the fingerprint set is a SUBSET of the archive set, every hashed file
// is present in the archive, so recomputation always matches.

const path = require('path');

// The package ships the COMPLETE buildable client source so anyone can reproduce
// the Game client CID from it (not just read the fairness core).
const includedRoots = [
  // Build + project configuration
  'package.json',
  'package-lock.json',
  'tsconfig.json',
  'config-overrides.js',
  '.env.production',
  // Reproducible-build toolchain (pinned Docker + instructions)
  'Dockerfile.repro',
  '.dockerignore',
  'REPRODUCIBLE_BUILD.md',
  // Licensing + docs
  'README.md',
  'FAIR_POKER_LICENSE.md',
  // Full application source (UI + fairness core + entry points). src/generated
  // is excluded below because it is regenerated deterministically at build time.
  'src',
  // Static assets + HTML the playable client build consumes. Generated evidence
  // files under public/ are excluded below.
  'public',
  // Scripts needed to regenerate metadata, build, prune and verify.
  'scripts/generate-release-metadata.js',
  'scripts/generate-audit-status.js',
  'scripts/create-ipfs-game-build.js',
  'scripts/create-source-release.js',
  'scripts/release-source-files.js',
  'scripts/reproducible-game-build.sh',
  'scripts/verify-transcript.js',
];

// Path parts never part of the public release (build artifacts, VCS, private
// infrastructure, regenerated output).
const EXCLUDED_PARTS = new Set([
  '.git',
  'node_modules',
  'build',
  'release',
  'coverage',
  'external',
  'ssh',
  'cloudflare-worker',
  'signal-server',
  '.github',
  // src/generated/* is regenerated by the build, so never ship or fingerprint it.
  'generated',
]);

// Generated / non-deterministic files under public/ that are pruned from the
// Game client anyway (see scripts/create-ipfs-game-build.js). Excluding them
// keeps the package small and its fingerprint clean and stable.
const EXCLUDED_PUBLIC_PREFIXES = ['public/source/', 'public/audit/'];
const EXCLUDED_PUBLIC_FILES = new Set([
  'public/ai.json',
  'public/ai-index.html',
  'public/ai-index.txt',
  'public/llms.txt',
  'public/sitemap.xml',
  // Evidence pages rewritten by sync-public-release-evidence.js with per-release
  // CIDs. They are pruned from the Game client anyway; excluding them keeps the
  // archive byte-identical across the release's two source-package passes (the
  // second pass only backfills the source CID into release.json).
  'public/audit-report.html',
  'public/security.html',
  'public/verify-guide.html',
]);

// Private release scripts that must never be published even if referenced.
const EXCLUDED_EXACT = new Set([
  'scripts/create-source-evidence.js',
  'scripts/sync-public-release-evidence.js',
  'scripts/validate-release-evidence.js',
]);

// Binary asset extensions whose contents need not be fingerprinted. They are
// still SHIPPED in the archive (the build needs them); only the fingerprint
// skips them.
const FINGERPRINT_SKIP_EXTENSIONS = new Set(['.png', '.ico']);

function toPosix(relative) {
  return relative.split(path.sep).join('/');
}

// True when a file (path relative to the app root) must NOT be part of the
// published release at all. Shared by the fingerprint and the archive.
function isExcludedFromRelease(relative) {
  const posix = toPosix(relative);
  const parts = posix.split('/');
  if (parts.some((part) => EXCLUDED_PARTS.has(part))) {
    return true;
  }
  if (parts.some((part) => part === '.DS_Store' || part.startsWith('._'))) {
    return true;
  }
  if (EXCLUDED_EXACT.has(posix)) {
    return true;
  }
  if (EXCLUDED_PUBLIC_FILES.has(posix)) {
    return true;
  }
  if (EXCLUDED_PUBLIC_PREFIXES.some((prefix) => posix.startsWith(prefix))) {
    return true;
  }
  return false;
}

// True when a file should be skipped for the FINGERPRINT but still shipped in
// the archive: excluded-from-release files plus binary assets.
function isExcludedFromFingerprint(relative) {
  if (isExcludedFromRelease(relative)) {
    return true;
  }
  return FINGERPRINT_SKIP_EXTENSIONS.has(path.extname(relative).toLowerCase());
}

// Transform applied to a file's bytes before it is fingerprinted AND archived.
// Both generate-release-metadata.js and create-source-release.js MUST call this
// so the hashed bytes and the shipped bytes are identical (no drift).
//
// .env.production: drop the mutable GAME_CID line. It is not needed to build the
// client (only REACT_APP_* values and GENERATE_SOURCEMAP affect the bundle), and
// shipping it would put a stale/duplicated release identity inside the package.
function releaseFileContent(posixRelative, buffer) {
  if (posixRelative === '.env.production') {
    return Buffer.from(buffer.toString('utf8').replace(/^GAME_CID=.*\r?\n?/m, ''));
  }
  return buffer;
}

module.exports = {
  includedRoots,
  isExcludedFromRelease,
  isExcludedFromFingerprint,
  releaseFileContent,
};
