type I18nContextValue = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: TranslationKey, params?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);
const STORAGE_KEY = 'fairpoker:language';

function normalizeLanguage(value: string | null | undefined): LanguageCode | undefined {
  const base = value?.toLowerCase().split('-')[0];
  return languages.some(language => language.code === base) ? base as LanguageCode : undefined;
}

function detectInitialLanguage(): LanguageCode {
  if (typeof window === 'undefined') {
    return 'zh';
  }
  return readStoredLanguage()
    ?? normalizeLanguage(window.navigator.language)
    ?? 'zh';
}

function readStoredLanguage(): LanguageCode | undefined {
  try {
    return normalizeLanguage(window.localStorage.getItem(STORAGE_KEY));
  } catch {
    return undefined;
  }
}

function writeStoredLanguage(language: LanguageCode) {
  try {
    window.localStorage.setItem(STORAGE_KEY, language);
  } catch {
    // Language choice should never prevent the homepage from rendering.
  }
}

export function translateKey(
  language: LanguageCode,
  key: TranslationKey,
  params?: Record<string, string | number>,
) {
  let text = dictionaries[language][key] ?? dictionaries.en[key] ?? dictionaries.zh[key] ?? key;
  Object.entries(params ?? {}).forEach(([name, value]) => {
    text = text.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value));
  });
  return text;
}

export function LanguageProvider(props: {children: React.ReactNode}) {
  const [language, setLanguageState] = useState<LanguageCode>(() => detectInitialLanguage());

  const value = useMemo<I18nContextValue>(() => {
    const setLanguage = (next: LanguageCode) => {
      setLanguageState(next);
      writeStoredLanguage(next);
      document.documentElement.lang = next;
    };
    const t = (key: TranslationKey, params?: Record<string, string | number>) => translateKey(language, key, params);
    return {language, setLanguage, t};
  }, [language]);

  React.useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  return <I18nContext.Provider value={value}>{props.children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    return {
      language: 'zh' as LanguageCode,
      setLanguage: () => undefined,
      t: (key: TranslationKey, params?: Record<string, string | number>) => translateKey('zh', key, params),
    };
  }
  return value;
}

const handRankKeys = [
  'handStraightFlush',
  'handFourOfAKind',
  'handFullHouse',
  'handFlush',
  'handStraight',
  'handThreeOfAKind',
  'handTwoPair',
  'handOnePair',
  'handHighCard',
] as const satisfies readonly TranslationKey[];

export function useHandRankLabel() {
  const {t} = useI18n();
  return useCallback((handValue: number | undefined) => {
    const key = handValue === undefined ? undefined : handRankKeys[handValue];
    return t(key ?? 'handUnknown');
  }, [t]);
}

export function localizeLegacyBilingualText(text: string | undefined, language: LanguageCode) {
  if (!text) {
    return '';
  }
  const separator = ' / ';
  if (!text.includes(separator)) {
    return text;
  }
  const [zhText, enText] = text.split(separator);
  return language === 'zh' ? zhText : enText || zhText;
}

export function useLocalizedText() {
  const {language} = useI18n();
  return useCallback((text: string | undefined) => localizeLegacyBilingualText(text, language), [language]);
}

export function LanguageSelect(props: {className?: string}) {
  const {language, setLanguage} = useI18n();
  return (
    <label className={props.className ?? 'language-select'}>
      <span className="sr-only">Language</span>
      <select
        value={language}
        onChange={event => setLanguage(event.target.value as LanguageCode)}
        aria-label="Language"
      >
        {languages.map(item => (
          <option value={item.code} key={item.code}>{item.label}</option>
        ))}
      </select>
    </label>
  );
}
